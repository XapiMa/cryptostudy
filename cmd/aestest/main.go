package main

import (
	"fmt"

	"github.com/mas9612/cryptostudy/aes"
)

func main() {
	plainText := []byte{0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff}
	key := []byte{0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f}
	fmt.Println("Before Cipher:")
	for _, b := range plainText {
		fmt.Printf("%02x ", b)
	}
	fmt.Print("\n\n")

	cipherText := make([]byte, len(plainText))
	aes.Cipher(plainText, cipherText, key)
	fmt.Println("After Cipher:")
	for _, b := range cipherText {
		fmt.Printf("%02x ", b)
	}
	fmt.Print("\n")
}
